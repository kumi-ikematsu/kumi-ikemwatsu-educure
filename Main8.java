/**
 * 名前・出身地・好きな食べ物を変数に格納し、
 * その内容を表示するプログラム。
 */
public class Main8 {
    public static void main(String[] args) {
        //見出しを表示
        System.out.println("===== 自己紹介 =====");
        //名前を表示
        String name = "池松 玖美";
        //出身地を表示
        String hometown = "福岡";
        //好きな食べ物を表示
        String favoriteFood = "ラーメン";
        
        System.out.println("名前: " + name);
        System.out.println("出身地: " + hometown);
        System.out.println("好きな食べ物: " + favoriteFood);
    }
}
